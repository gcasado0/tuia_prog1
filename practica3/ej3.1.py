'''
3.3 Solicitar un numero del 1 al 7 y diga el dıa de la semana correspondiente. 
Suponga que el lunes es el primer dıa.
'''

dia = int(input("Ingrese un numero del 1 al 7: "))
if dia == 1:
    print("Lunes")
elif dia==2:
    print("Martes")
elif dia==3:
    print("Miercoles")
elif dia==4:
    print("Jueves")    
elif dia==5:
    print("Viernes")    
elif dia==6:
    print("Sabado")    
elif dia==7:
    print("Domingo")    